public enum TokenType {

    SPACES, // пробел
    NUMBER, // число
    ADD, // символ "+"
    SUB, // символ "-"
    MUL, // символ "*"
    DIV, // символ "/"
    EXCLAM, // символ "!"
    SEMICOLON, // символ ";"
    LPAR, // символ "("
    RPAR, // символ ")"
    VAR, // переменная
    ASSIGN, // приравнивание ":="
}